# if / elif       /else
# se / se não se / se não
# O 'elif' e o 'else' dependem do if
# O else sempre será a ultima condição a ser executada.

condicao1 = False
condicao2 = False
condicao3 = True
condicao4 = False

if condicao1:
    print('Código para condição 1')
elif condicao2:
    print('Código para condição2')
elif condicao3:
    print('Código para condição3')
elif condicao4:
    print('Código para condição4')
else:
    print('nenhuma condição foi satisfeita')


